import java.util.*;
class switchcase
{
	public static void main(String[]args)
	{
		int n=22;
		switch(n)
		{
			case 5:
			    System.out.println("Printing 5");
			break;	
			case 10:
			    System.out.println("Printing 10");
			break;	
			case 15:
			    System.out.println("Printing 15");
			break;	
			case 20:
			    System.out.println("Printing 20");
			break;	
			
		default:
		     System.out.println("Invalid");
		}	 
	}
}	